package Globals;

use strict;

use Exporter;
use vars qw(@ISA @EXPORT @EXPORT_OK);
@ISA = qw(Exporter);
@EXPORT = qw($verbosity);
@EXPORT_OK = qw(
$TAG
$config_ref
$VERSION
$RELEASE_DATE
$WORKSPACE
);

#######################################################################################
# MODULES USED
#######################################################################################
use Utils;

#######################################################################################
# PACKAGE GLOBALS
#######################################################################################
use vars qw(
$TAG
$verbosity
$config_ref
$VERSION
$RELEASE_DATE
$WORKSPACE
);

#--------------------------------------------------------------------------------------
# Function: globals_init
# Synopsys: Initialize package's global variables
#--------------------------------------------------------------------------------------
sub globals_init {
    $TAG = undef;
    $verbosity = 1;
    $config_ref = {};
    $VERSION = "UNKNOWN";
    $RELEASE_DATE = "UNKNOWN";
    use Cwd; $WORKSPACE = cwd();
}

#######################################################################################
# INITIALIZATION
#######################################################################################
globals_init();

# Package BEGIN must return true value
return 1;
